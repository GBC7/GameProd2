function drawTheMap()
{
    let destX = 0, destY = 0;

    for (let row = 0; row < map.length; row++)
    {
        for (let col = 0; col < map[0].length; col++)
        {
            switch (map[row][col])
            {
                case 0:
                    ctx.drawImage(roof, destX, destY, 32, 32);
                    break;
                case 1:
                    ctx.drawImage(outsideWall, destX, destY, 32, 32);
                    break;
                /*case 2:
                    ctx.drawImage(floor, destX, destY, 32, 32);
                    break;
                case 3:
                    ctx.drawImage(drain, destX, destY, 32, 32);
                    break;*/
            }

            destX += 32;
        }
        destX = 0;
        destY += 32;
    }
    drawPMap();
}

function drawPMap()
{
    let destX = 0, destY = 0;

    for (let row = 0; row < pMap.length; row++)
    {
        for (let col = 0; col < pMap[0].length; col++)
        {
            switch (pMap[row][col])
            {
                case 1:
                    //Sets position on tile sheet to pick from when drawing player
                    p.srcX = p.width * (p.frameX % 4);
                    p.srcY = p.height * p.frameY;
                    ctx.drawImage(scientist, p.srcX, p.srcY, p.width, p.height, destX, destY, p.width, p.height);
                    break;
            }

            destX += 8;
        }
        destX = 0;
        destY += 8;
    }
}

function movePlayer()
{
    p.prevCol = p.col;//Set column to clear
    p.prevRow = p.row;//Set row to clear

    if (p.left)
    {
        if (p.col > 0)
        {
            //remove player from current column
            pMap[p.row][p.col] = 0;
            //update player column
            p.col --;
            //add player to updated row
            pMap[p.row][p.col] = 1;
        }
        //Change tile sheet frame to show player walking right
        p.frameY = 1;
        //Increment in order to flip through the walking tiles for this direction
        p.frameX ++;
    }

    if (p.right)
    {
        if (p.col < 96)
        {
            //remove player from current column
            pMap[p.row][p.col] = 0;
            //update player column
            p.col ++;
            //add player to updated column
            pMap[p.row][p.col] = 1;
        }
        //Change tile sheet frame to show player walking right
        p.frameY = 2;
        //Increment in order to flip through the walking tiles for this direction
        p.frameX ++;
    }

    if (p.up)
    {
        if (p.row > 0)
        {
            //remove player from current row
            pMap[p.row][p.col] = 0;
            //update player row
            p.row --;
            //add player to updated row
            pMap[p.row][p.col] = 1;
        }
        //Change tile sheet frame to show player walking up
        p.frameY = 3;
        //Increment in order to flip through the walking tiles for this direction
        p.frameX ++;
    }

    if (p.down)
    {
        if (p.row < 69)
        {
            //remove player from current row
            pMap[p.row][p.col] = 0;
            //update player row
            p.row ++;
            //add player to updated row
            pMap[p.row][p.col] = 1;

        }
        //Change tile sheet frame to show player walking down
        p.frameY = 0;
        //Increment in order to flip through the walking tiles for this direction
        p.frameX ++;
    }
    ctx.clearRect(p.prevCol, p.prevRow, p.width, p.height);//Clear portion of canvas the player was last on
    drawTheMap();//Redraw entire canvas -- this function also calls the function to draw the players map (pMap)
}

function onKeyDown(e)
//Maybe change take out onKeyUp function and have all commands in onKeyDown so that
//  you can not go diagonally -- character will always hover when going diagonally
//  unless new sprite sheet is found/made with character walking diagonally
{
    if (e.keyCode === 37)//Left

    {
        p.left = true;
    }

    else if (e.keyCode === 39)//Right

    {
        p.right = true;
    }

    else if (e.keyCode === 38)//Up

    {
        p.up = true;
    }

    else if (e.keyCode === 40)//Down

    {
        p.down = true;
    }
    movePlayer();
}

function onKeyUp(e)
{
    if (e.keyCode === 37)//Left

    {
        p.left = false;
        //Reset frame so that the player starts the correct spot in the tile sheet for next direction
        p.frameX = 0;
    }

    else if (e.keyCode === 39)//Right

    {
        p.right = false;
        //Reset frame so that the player starts the correct spot in the tile sheet for next direction
        p.frameX = 0;
    }

    else if (e.keyCode === 38)//Up

    {
        p.up = false;
        //Reset frame so that the player starts the correct spot in the tile sheet for next direction
        p.frameX = 0;
    }

    else if (e.keyCode === 40)//Down

    {
        p.down = false;
        //Reset frame so that the player starts the correct spot in the tile sheet for next direction
        p.frameX = 0;
    }
}







